public class prize
		{
			String prize;
			int value;
			public prize(String p, int v)
				{
					prize=p;
					value=v;
				}
			public String getPrize()
				{
					return prize;
				}
			public void setPrize(String suit)
				{
					this.prize = suit;
				}
			public int getValue()
				{
					return value;
				}
			public void setValue(int value)
				{
					this.value = value;
				}
		}

	
